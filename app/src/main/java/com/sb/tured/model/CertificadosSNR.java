package com.sb.tured.model;

public class CertificadosSNR {

    private String id;
    private String ciudad;
    private String codigo;
    private String estatus;
    private String circuloregistral;
    private String matricula;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getEstatus() {
        return estatus;
    }

    public void setEstatus(String estatus) {
        this.estatus = estatus;
    }

    public String getCirculoregistral() {
        return circuloregistral;
    }

    public void setCirculoregistral(String circuloregistral) {
        this.circuloregistral = circuloregistral;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }
}
