package com.sb.tured.model;

public class PackOperRecarga {

    private String id;
    private String valor;
    private String descripcion;
    private String codigo;
    private String estatus;
    private String id_producto_recargas;
    private String categoria;
    private String orden_categoria;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getEstatus() {
        return estatus;
    }

    public void setEstatus(String estatus) {
        this.estatus = estatus;
    }

    public String getId_producto_recargas() {
        return id_producto_recargas;
    }

    public void setId_producto_recargas(String id_producto_recargas) {
        this.id_producto_recargas = id_producto_recargas;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getOrden_categoria() {
        return orden_categoria;
    }

    public void setOrden_categoria(String orden_categoria) {
        this.orden_categoria = orden_categoria;
    }
}
