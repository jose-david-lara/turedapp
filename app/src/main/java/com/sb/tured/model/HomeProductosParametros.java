package com.sb.tured.model;

public class HomeProductosParametros {

    private String id;
    private String n;
    private String t;
    private String lmn;
    private String lmx;
    private String tabla;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }

    public String getLmn() {
        return lmn;
    }

    public void setLmn(String lmn) {
        this.lmn = lmn;
    }

    public String getLmx() {
        return lmx;
    }

    public void setLmx(String lmx) {
        this.lmx = lmx;
    }

    public String getTabla() {
        return tabla;
    }

    public void setTabla(String tabla) {
        this.tabla = tabla;
    }
}
